                        <p>Dokter Bola – Real Madrid dirumorkan berani membayar Granada jika berhasil mengalahkan Barcelona di pertandingan terakhir La Liga Spanyol musim ini.<br><br>
                            Dokter Bola mengutip pernyataan Mantan Presiden Barcelona, Joan Gaspart, dari Marca, yang mengatakan Real Madrid menunjukkan gelagat membahayakan sepakbola dan menilai tindakan tersebut tidak terpuji.<br><br>
                            Dari sekian banyak liga bergengsi di Eropa, hanya La Liga Spanyol saja yang masih belum diketahui siapa pemenangnya. Persaingan sampai dengan pertandingan terakhir masih ketat dan menjadi milik Barcelona dan Real Madrid. Barcelona saat ini bercokol di puncak klasemen dengan selisih satu poin saja dari Real Madrid yang ada di posisi dua.<br><br>
                            Pertandingan terakhir untuk keduanya akan digelar pada waktu yang sama, Sabtu (14/05/2016) pukul 22.00 Waktu Indonesia Barat. Barcelona akan melakukan pertandingan tandang melawan Granada, sementara Madrid akan bertandang ke Stadion Municipal de Riazor, La Coruna.<br><br>
                            Menanggapi hal ini Presiden La Liga, Javier Tebas, mengatakan bahwa Madrid akan mengikuti aturan dan peraturan Spanyol tidak memperbolehkan sesuatu yang dinamakan “bonus”.<br><br>
                            Pemain Barcelona, Luis Suarez juga mengatakan rumor tersebut merupakan sampah yang tidak sepatutnya didengar. Dikutip dari http://thesun.co.uk, Suarez mengatakan saat konferensi pers, “Saya tidak percaya dengan hal seperti ini, setiap tim bermain untuk kebanggaan mereka sendiri. Para pemain selalu ingin keluar (bermain) dan menang. Setiap pemain tahu apa yang mereka harus lakukan. Saya tidak pernah mendengar tentang hal ini sebelum dating ke Spanyol. Kami ingin menang dan Granada ingin mengakhiri musim mereka dengan catatan kemenangan.”<br><br>
                            Rumor match fixing atau semacam pemberian bonus ini juga pernah berhembus pada akhir musim 1992-1993. Saat itu, Barcelona memenangi La Liga setelah Real Madrid kalah dari Tenerife di pertandingan terakhir musim tersebut. Namun Joan Gaspart menyangkal telah membayar “insentif” kepada Tenerife.</p>
